#include "3-calc.h"

/* Adds two integers */
int op_add(int a, int b)
{
	return (a + b);
}

/* Subtracts two integers */
int op_sub(int a, int b)
{
	return (a - b);
}

/* Multiplies two integers */
int op_mul(int a, int b)
{
	return (a * b);
}

/* Divides two integers */
int op_div(int a, int b)
{
	return (a / b);
}

/* Modulo of two integers */
int op_mod(int a, int b)
{
	return (a % b);
}
